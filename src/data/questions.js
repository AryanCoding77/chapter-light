// Helper function to get questions by difficulty
export const getQuestionsByDifficulty = (difficulty) => {
  return questionBank.filter(q => q.difficulty === difficulty)
}

// Helper function to get random question (avoiding recently asked)
export const getRandomQuestion = (difficulty, askedQuestionIds = []) => {
  const questions = getQuestionsByDifficulty(difficulty)
  const availableQuestions = questions.filter(q => !askedQuestionIds.includes(q.id))
  
  // If all questions have been asked, reset and use all questions
  const questionsToUse = availableQuestions.length > 0 ? availableQuestions : questions
  
  return questionsToUse[Math.floor(Math.random() * questionsToUse.length)]
}
